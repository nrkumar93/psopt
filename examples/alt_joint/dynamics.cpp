adouble ad_fmod(adouble x, adouble y)
{
  struct u {adouble f; uint64_t i;} ux, uy;
  ux.f = x;
  uy.f = y;
  int ex = ux.i>>52 & 0x7ff;
  int ey = uy.i>>52 & 0x7ff;
  int sx = ux.i>>63;
  uint64_t i;

  /* in the followings uxi should be ux.i, but then gcc wrongly adds */
  /* float load/store to inner loops ruining performance and code size */
  uint64_t uxi = ux.i;

  if (uy.i<<1 == 0 || isnan(y.value()) || ex == 0x7ff)
    return (x*y)/(x*y);
  if (uxi<<1 <= uy.i<<1) {
    if (uxi<<1 == uy.i<<1)
      return 0*x;
    return x;
  }

  /* normalize x and y */
  if (!ex) {
    for (i = uxi<<12; i>>63 == 0; ex--, i <<= 1);
    uxi <<= -ex + 1;
  } else {
    uxi &= -1ULL >> 12;
    uxi |= 1ULL << 52;
  }
  if (!ey) {
    for (i = uy.i<<12; i>>63 == 0; ey--, i <<= 1);
    uy.i <<= -ey + 1;
  } else {
    uy.i &= -1ULL >> 12;
    uy.i |= 1ULL << 52;
  }

  /* x mod y */
  for (; ex > ey; ex--) {
    i = uxi - uy.i;
    if (i >> 63 == 0) {
      if (i == 0)
        return 0*x;
      uxi = i;
    }
    uxi <<= 1;
  }
  i = uxi - uy.i;
  if (i >> 63 == 0) {
    if (i == 0)
      return 0*x;
    uxi = i;
  }
  for (; uxi>>52 == 0; uxi <<= 1, ex--);

  /* scale result */
  if (ex > 0) {
    uxi -= 1ULL << 52;
    uxi |= (uint64_t)ex << 52;
  } else {
    uxi >>= -ex + 1;
  }
  uxi |= (uint64_t)sx << 63;
  ux.i = uxi;
  return ux.f;
}

/*!
 * \brief normalize_angle_positive
 *
 *        Normalizes the angle to be 0 to 2*M_PI
 *        It takes and returns radians.
 */
static inline adouble normalize_angle_positive(adouble angle)
{
  return ad_fmod(ad_fmod(angle, 2.0*M_PI) + 2.0*M_PI, 2.0*M_PI);
}


